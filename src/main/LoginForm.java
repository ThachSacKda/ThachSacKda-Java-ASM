    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String user = txtUsr.getText();
        String password = new String(txtPass.getPassword());
        Goku m = null;
        boolean login = false;
        User admin = new User("kda", "123");
        User checkUser = new User(user, password);
        StringBuilder sb = new StringBuilder();
        
    
        if(admin.equals(checkUser)){
            m = new Goku();
            login = true;
        }
        if (user.equals("")) {
            sb.append("Username is empty \n");

        }
        if (password.equals("")) {
            sb.append("Password empty \n");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Ivailedation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (login) {
            
            this.dispose();
            
            m.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "User name or Password is invalid");
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

