package model;

import java.io.Serializable;

/**
 *
 * @author ACER
 */
public class modelGoku implements  Serializable {
    private String ID;
    private String Name;
    private Double Price;
    private String Type;

    public modelGoku(String ID, String Name, Double Price, String Type) {
        this.ID = ID;
        this.Name = Name;
        this.Price = Price;
        this.Type = Type;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public Double getPrice() {
        return Price;
    }

    public void setPrice(Double Price) {
        this.Price = Price;
    }

    public String getType() {
        return Type;
    }

    public void setType(String Type) {
        this.Type = Type;
    }
    
}
